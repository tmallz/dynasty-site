				<li>$60 for 2nd place (20%)</li>
			</ul>
			<p class="mt-2">
				Note: This is to be paid to the managers who come in first and second in the playoffs, not
				the regular season.
			</p>

			<h3 class="mt-4 text-xl font-semibold underline">6.2 Money Management</h3>
			<p class="mt-2">
				Dues will be paid to the commissioner before the start of each year, and then the
				commissioner will pay out the winners at the end of the year.
			</p>
